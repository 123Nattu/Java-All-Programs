package loops;

public class Myswitchcase {
	public static void main(String[] args) {
		String day="one";
		switch(day)
		{
		case "one":
		{
			System.out.println("Monday");
			break;
		}
		
		case "two":
		{
			System.out.println("Tuesday");
			break;
		}
		
		case "Three":
		{
			System.out.println("wednesday");
			break;
		}
		
		default:
		{
			System.out.println("Holiday");
			break;
		}
		
	}
	}
}


